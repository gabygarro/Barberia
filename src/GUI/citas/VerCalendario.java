    public static final int SEMANA = 0;
    public static final int MES = 1;
    private ArrayList<Cita> citas;
    private LocalDateTime fechaInicio;
    
    public VerCalendario(Main main, int modo, LocalDateTime fechaInicio) {
        try{
            if (main.getBarberia().getCitas().size()==0){
                throw new RuntimeException("No hay citas a√∫n");
            }
        initComponents();
        this.fechaInicio = fechaInicio;
        this.setVisible(true);
       
        if (modo == SEMANA){
            citas = main.getBarberia().getAgenda().getCitasSemana(fechaInicio);
        }else{
            citas = main.getBarberia().getAgenda().getCitasMes(fechaInicio);
            System.out.println(citas.size());
        }
        initTabla();
        }
        catch(RuntimeException e){
            JOptionPane.showMessageDialog(this, "No ha creado citas", "No hay citas", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    private void initTabla(){
        DefaultTableModel modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
       
                return false;
            }
        };
        
        modelo.addColumn("Fecha");
        modelo.addColumn("Cliente");
        modelo.addColumn("Correo");
        DateTimeFormatter dt =DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm");
        try{    
        for (Cita c : citas){
            modelo.addRow(new Object[] {c.getFecha().format(dt), c.getCliente().getNombre(), c.getCliente().getCorreo()});
        }
        }
        catch (NullPointerException e){
            
        }
        jTable1.setModel(modelo);
                
                
    }
    

